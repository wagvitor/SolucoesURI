#include <stdio.h>
  
int main() {
  double A, B, C, MEDIA;
 
    A, B, C >= 0 && A, B, C <= 10.0;
 
    scanf("%lf",&A);
    scanf("%lf",&B);
    scanf("%lf",&C);
 
    if ((A <= 10.0) && (B <= 10.0) && (C <= 10.0))
    MEDIA = ((A * 2.0) + (B * 3.0) + (C*5.0))/10;
 
 
    printf("MEDIA = %.1f\n",MEDIA);
  
    return 0;
}
